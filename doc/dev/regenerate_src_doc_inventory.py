#!/usr/bin/env python3
"""Regenerate doc/dev/src_folder_doc_inventory.md from the current src/ tree."""

from __future__ import annotations

from pathlib import Path

ROOT = Path(__file__).resolve().parents[2] / "src"
OUT = Path(__file__).resolve().parent / "src_folder_doc_inventory.md"
EXCLUDE_PARTS = frozenset({"__pycache__", ".claude", ".desloppify", ".git"})


def main() -> None:
    missing_agents: list[str] = []
    missing_readme: list[str] = []

    for d in sorted(ROOT.rglob("*")):
        if not d.is_dir():
            continue
        rel = d.relative_to(ROOT)
        if any(p in EXCLUDE_PARTS for p in rel.parts):
            continue
        if len(rel.parts) >= 2 and rel.parts[0] == "output":
            continue
        has_agents = (d / "AGENTS.md").exists()
        has_readme = (d / "README.md").exists()
        py_here = list(d.glob("*.py"))
        subdirs = [x for x in d.iterdir() if x.is_dir() and x.name not in EXCLUDE_PARTS]
        is_pkg = (d / "__init__.py").exists() or any(
            (sd / "__init__.py").exists() for sd in subdirs
        )
        if not py_here and not subdirs and not is_pkg:
            continue
        if not has_agents:
            missing_agents.append(str(d))
        if not has_readme:
            missing_readme.append(str(d))

    lines = [
        "# src/ folder documentation inventory",
        "",
        "Generated by [`regenerate_src_doc_inventory.py`](regenerate_src_doc_inventory.py).",
        "Excludes `__pycache__`, `.claude`, `.desloppify`, and nested `src/output/*` artifact trees.",
        "Directories listed contain `.py` files and/or subdirectories (package-like).",
        "",
        f"## Missing AGENTS.md ({len(missing_agents)})",
        "",
    ]
    lines.extend(f"- `{p}`" for p in missing_agents)
    lines.extend(["", f"## Missing README.md ({len(missing_readme)})", ""])
    lines.extend(f"- `{p}`" for p in missing_readme)
    lines.extend(
        [
            "",
            "## Notes",
            "",
            "- Add scaffold `AGENTS.md` / `README.md` when a directory becomes a maintained package.",
        ]
    )
    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {OUT} ({len(missing_agents)} missing AGENTS, {len(missing_readme)} missing README)")


if __name__ == "__main__":
    main()
